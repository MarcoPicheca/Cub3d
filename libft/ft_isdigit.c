#include "libft.h"

int	ft_isdigit(int c)
{
	if ((c <= 57 && c >= 48))
		return (1);
	return (0);
}

int	ft_isspace(int c)
{
	if (c == 32 || c == '\t')
		return (1);
	return (0);
}
